    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        $data   = SubCategory::orderBy('sub_categories.name')
                    ->select(
                                'sub_categories.id',
                                'sub_categories.name',
                                'sub_categories.active'
                            )
                    ->get();

        foreach ($data as $key => $value) {
            if ($value->active == 1) {
                $value->active = "".'<span class="badge badge-success">Active</span>'."";
            }else{
                $value->active = "".'<span class="badge badge-danger">Inactive</span>'."";
            }
        }
        return 
            DataTables::of($data)
                ->addColumn('action',function($data){
                    return '
                    <div class="btn-group btn-group">
                        <a class="btn btn-info btn-xs" href="sub_categories/'.$data->id.'">
                            <i class="fa fa-eye"></i>
                        </a>
                        <a class="btn btn-info btn-xs" href="sub_categories/'.$data->id.'/edit" id="'.$data->id.'">
                            <i class="fas fa-pencil-alt"></i>
                        </a>
                        <button
                            class="btn btn-danger btn-xs delete_all"
                            data-url="'. url('del_category') .'" data-id="'.$data->id.'">
                            <i class="fas fa-trash-alt"></i>
                        </button>
                    </div>';
                })
                ->addColumn('srno','')
                ->rawColumns(['srno','','action', 'active'])
                ->make(true);
    }

